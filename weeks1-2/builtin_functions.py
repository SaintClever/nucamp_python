print("Math Functions")

# absolute turns a negative number to a positive number
abs_int = abs(-1)
print(abs_int)

int_to_float = float(100)
print(int_to_float)

float_to_int = int(1.23)
print(float_to_int)
print(int(1.23))  # or without the variable

print(max(1, 2, -5, 10, 0))
print(min(1, 2, -5, 10, 0))
print(pow(2, 3))
print(round(51.6))
